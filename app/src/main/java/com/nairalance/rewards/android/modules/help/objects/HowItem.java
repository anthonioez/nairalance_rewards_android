package com.nairalance.rewards.android.modules.help.objects;

public class HowItem
{
    public int icon;
    public int title;
    public int desc;

    public HowItem()
    {
        this.icon = 0;
        this.title = 0;
        this.desc = 0;
    }

    public HowItem(int icon, int title, int desc)
    {
        this.icon = icon;
        this.title = title;
        this.desc = desc;
    }

}

